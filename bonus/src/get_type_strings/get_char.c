char	*get_char(t_options *options, va_list *arg_ptr)
{
	const char	c = (char)va_arg(*arg_ptr, int);
	char		*str;

	(void)options;
	str = malloc(2);
	str[2 - 1] = '\0';
	str[0] = c;
	return (str);
}

