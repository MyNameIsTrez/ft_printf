#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

void	test_get_char_helper(char *expected, ...)
{
	va_list	arg_ptr;

	va_start(arg_ptr, expected);
	ASSERT_STR(get_char(&arg_ptr), expected);
	va_end(arg_ptr);
}

////////////////////////////////////////////////////////////////////////////////

void	test_get_char(void)
{
	test_get_char_helper("a", 'a');
}

