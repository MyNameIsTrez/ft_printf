#include "ft_printf_bonus.h"

////////////////////////////////////////////////////////////////////////////////

void	get_char(va_list arg_ptr, t_conversion *conversion)
{
	char const	chr = (char)va_arg(arg_ptr, int);

	conversion->base_str = ft_char_to_str(chr);
	conversion->prefix = ft_strdup("");
}

