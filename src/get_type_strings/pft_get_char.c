////////////////////////////////////////////////////////////////////////////////

#include "ft_printf.h"

////////////////////////////////////////////////////////////////////////////////

void	pft_get_char(va_list arg_ptr, t_options *options)
{
	char const	chr = (char)va_arg(arg_ptr, int);

	options->parts.base_str = ft_char_to_str(chr);
	options->parts.prefix = ft_empty_str();
}

