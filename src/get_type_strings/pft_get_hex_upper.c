t_status	pft_get_hex_upper(va_list arg_ptr, t_options *options)
{
	if (pft_get_hex_lower(arg_ptr, options) != OK)
		return (ERROR);
	ft_str_upper(options->parts.base_str);
	ft_str_upper(options->parts.prefix);
	return (OK);
}

